package com.yatish.nativeFunctions;

public class J1_What_is_Native_Function {

	/*
	 * What are native functions?
	 * Native functions are the functions which are written in other programming language like C or C++ which JVM doesn't understand. JVM will call the function implementation in native language(C/C++)
	 * and return the result.
	 * 
	 * eg:
	 * class ABC { 
	 * 
	 * 		public native int someMethod();		-->  This will call the 'someMethod()' defined in C/C++ and return the result(int). 
	 * 
	 * }
	 * 
	 */
}
